    private void txtDisAmtPerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtDisAmtPerActionPerformed
        // TODO add your handling code here:
        double discount;
        if (rdbAmount.isSelected()) {
            if (!txtDisAmtPer.getText().isEmpty()) {
                discount = Double.parseDouble(txtDisAmtPer.getText());
                if (this.subTotal < discount) {
                    JOptionPane.showMessageDialog(null, "Discount amount bigger than sub total", "cannot proceed", JOptionPane.INFORMATION_MESSAGE);
                } else {
                    calculateTotal();
                    txtRemark.requestFocus();
                }
            } else {
                JOptionPane.showMessageDialog(null, "Please enter discount amount", "Discount empty", JOptionPane.INFORMATION_MESSAGE);
            }
        } else {
            if (!txtDisAmtPer.getText().isEmpty()) {
                discount = this.subTotal * Double.parseDouble(txtDisAmtPer.getText()) / 100;
                if (this.subTotal < discount) {
                    JOptionPane.showMessageDialog(null, "Discount amount bigger than sub total", "cannot proceed", JOptionPane.INFORMATION_MESSAGE);
                } else {
                    calculateTotal();
                }
            } else {
                JOptionPane.showMessageDialog(null, "Please enter discount precentage", "Discount empty", JOptionPane.INFORMATION_MESSAGE);
            }
        }
    }//GEN-LAST:event_txtDisAmtPerActionPerformed

    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        if (!txtDisAmtPer.getText().isEmpty()) {
            if (Double.parseDouble(txtDisAmtPer.getText()) > 0) {
                if (!txtRemark.getText().isEmpty()) {
                    saveDiscount();
                } else {
                    JOptionPane.showMessageDialog(this, "Please enter discount reason..", "Remarks empty", JOptionPane.INFORMATION_MESSAGE);
                }
            } else {
                JOptionPane.showMessageDialog(this, "Please enter valid discount..", "Invalid discount", JOptionPane.INFORMATION_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(this, "Please enter discount amount or presentage..", "Discount empty", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_btnSaveActionPerformed

