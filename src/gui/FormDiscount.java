    private void calculateTotal() {
        if (rdbAmount.isSelected()) {
            this.subTotal = Double.parseDouble(txtGrandTotal.getText());
            this.discountAmount = Double.parseDouble(txtDisAmtPer.getText());
            this.subTotal = this.subTotal - this.discountAmount;
        } else {                  //(2000/10)*100
            this.subTotal = Double.parseDouble(txtGrandTotal.getText());
            this.discountAmount = (this.subTotal * Double.parseDouble(txtDisAmtPer.getText())) / 100;
            this.subTotal = this.subTotal - this.discountAmount;
        }

        txtSubAfterDis.setText(Validator.BuildTwoDecimals(this.subTotal));
    }

    private void saveDiscount() {
//        FormMainFrame.discount = this.discountAmount;
//        FormMainFrame.grandTotal = this.subTotal;
//        FormMainFrame.txtDiscount.setText(Validator.BuildTwoDecimals(this.discountAmount));
//        FormMainFrame.txtGrandTotal.setText(Validator.BuildTwoDecimals(this.subTotal));
        if (!isItemDiscount) {
            FormBilling.discount = this.discountAmount;
            FormBilling.grandTotal = this.subTotal;
            FormBilling.txtDiscount.setText(Validator.BuildTwoDecimals(this.discountAmount));
            FormBilling.txtGrandTotal.setText(Validator.BuildTwoDecimals(this.subTotal));
            FormBilling.discount_remark = txtRemark.getText();
        }else{
            FormBilling.tblItemCart.setValueAt(Validator.BuildTwoDecimals(this.discountAmount), selectedRow, 4);
            FormBilling.tblItemCart.setValueAt(Validator.BuildTwoDecimals(this.subTotal), selectedRow, 5);
            FormBilling.grandTotal -= this.discountAmount;            
            FormBilling.subTotal -= this.discountAmount;            
            FormBilling.txtGrandTotal.setText(Validator.BuildTwoDecimals(FormBilling.grandTotal));
            FormBilling.txtSubTotal.setText(Validator.BuildTwoDecimals(FormBilling.subTotal));
            FormBilling.itemDiscountRemarkMap.put(selectedRow, txtRemark.getText());
        }
        this.dispose();
    }
